<?php

    echo "<h3>Operator Logika</h3>";

    var_dump(true && true); // true, karena kedua nilai benar

    echo "<br>";

    var_dump(true and false); // false, karena salah satu nilai salah

    echo "<br>";

    var_dump(true || false); // true, karena salah satu nilai benar

    echo "<br>";

    var_dump(true or false); // true, karena salah satu nilai benar

    echo "<br>";

    var_dump(!true); // false, karena negasi dari true adalah false

    echo "<br>";

    var_dump(!false); // true, karena negasi dari false adalah true